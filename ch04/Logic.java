package ch04;

public class Logic {
	public static void main(String[] args) {

		boolean a = true;
		boolean b = false;

		System.out.println("a && b는 " + (a && b));
		System.out.println("a & b는 " + (a & b));
		System.out.println("a || b는 " + (a || b));
		System.out.println("a | b는 " + (a | b));
		System.out.println("!a는 " + !a);
	}
}
